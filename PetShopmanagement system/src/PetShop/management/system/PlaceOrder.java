    private void btnAddToCartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddToCartActionPerformed
        // TODO add your handling code here:
        String name= txtProName.getText();
        String price = txtProPrice.getText();
        String totalprice = txtProTotal.getText();
        String quantity = String.valueOf(jSpinner1.getValue());
        String Qtype = this.Qtype.getText();
        String brand = (String) jComboBox1.getSelectedItem();
        DefaultTableModel dtm =(DefaultTableModel) jTable2.getModel();
        dtm.addRow(new Object[]{name,price,quantity,Qtype,totalprice,brand});
        grandTotal = grandTotal + Integer.parseInt(totalprice);
        lblGrandTotal.setText(String.valueOf(grandTotal));
                btnGenerateBillPrint.setEnabled(true);


        //update stock
        int quantityPro=(Integer)jSpinner1.getValue();
        Product product = ProductDao.getProductByQuantity(name);
        int ogvalue = Integer.parseInt(product.getQuantity());
        String NewValue=String.valueOf(ogvalue-quantityPro);
        int reCheck=ogvalue-quantityPro;
        ProductDao.updateProduct(name, NewValue);
        String id= String.valueOf(product.getId());
        if(reCheck==0){
        ProductDao.deleteStock(id);
        }
        
        clearProductFields();
        jSpinner1.setValue(1);
        txtProTotal.setText("");
        validateFields();
        
    }//GEN-LAST:event_btnAddToCartActionPerformed

