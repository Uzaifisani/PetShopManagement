    private void btnGenerateBillPrintActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerateBillPrintActionPerformed
        // TODO add your handling code here:
        String customerName = txtCusName.getText();
        String customerMobileNumber= txtCusMobileNo.getText();
        String customerEmail = txtCustEmail.getText();
        SimpleDateFormat dFormat = new SimpleDateFormat("dd-MM-yyyy");
        Date date= new Date();
        String todayDate=dFormat.format(date);
        String total =String.valueOf(grandTotal);
        String createdBy= userEmail;
        String brand = String.valueOf(lblBrand);
        Bill bill= new Bill();
        bill.setId(billId);
        bill.setName(customerName);
        bill.setMobileNumber(customerMobileNumber);
        bill.setEmail(customerEmail);
        bill.setDate(todayDate);
        bill.setTotal(total);
        bill.setCreatedBy(createdBy);
        BillDao.save(bill);
        
        
        //create doc
        String path ="F:\\PetShop management system\\Bill_Report\\";
        com.itextpdf.text.Document doc = new com.itextpdf.text.Document();
        try{
            PdfWriter.getInstance(doc, new FileOutputStream(path + ""+billId + ""+".pdf"));
            doc.open();
            Image img =Image.getInstance("F:\\PetShop management system\\PetShopmanagement system\\src\\images\\logo.png");
            img.setAlignment(Image.MIDDLE);
            doc.add(img);
            Paragraph shopName = new Paragraph("                                                            Pet Shop Bill\n");
            doc.add(shopName);
            Paragraph starLine= new Paragraph("****************************************************************************************************************");
            doc.add(starLine);
            Paragraph paragraph3 = new Paragraph("\tBill Id:"+billId+"\nCustomer Name: "+customerName+"n Total Paid: "+grandTotal);
            doc.add(paragraph3);
            doc.add(starLine);
            PdfPTable tb1= new PdfPTable(5);
            tb1.addCell("Product Name");
            tb1.addCell("Price");
            tb1.addCell("Quantity");
            tb1.addCell("QType");
            tb1.addCell("Total");
            tb1.addCell("Brand");
            for(int i=0;i< jTable2.getRowCount();i++){
             String n = jTable2.getValueAt(i, 0).toString();
             String d = jTable2.getValueAt(i, 1).toString();
             String f = jTable2.getValueAt(i, 2).toString();
             String c = jTable2.getValueAt(i, 3).toString();
             String e = jTable2.getValueAt(i, 4).toString();
             String g = jTable2.getValueAt(i, 5).toString();
             tb1.addCell(n);
             tb1.addCell(d);
             tb1.addCell(f);
             tb1.addCell(c);
             tb1.addCell(e);
            }
            doc.add(tb1);
            doc.add(starLine);
            
            Paragraph thanksMsg= new Paragraph("Thank You, Please Visit Again.");
            doc.add(thanksMsg);
            //open pdf
            //OpenPdf.openById(String.valueOf(billId));
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        doc.close();
        setVisible(false);
        new PlaceOrder(createdBy).setVisible(true);
        

    }//GEN-LAST:event_btnGenerateBillPrintActionPerformed

